package sk.kosickaakademia.onofrej.generic;

public class Algorithm {
    public int add(int a, int b){
        return a+b;
    }

    // overload
    public double add(double a, double b){
        return a+b;
    }

    // overload
    public int add(int a, int b, int c){
        return a+b+c;
    }
}
